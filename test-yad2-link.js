function extractYad2Link(text) {
  const yad2Patterns = [
    /https?:\/\/(?:www\.)?yad2\.co\.il\/[^\s,]+/gi,
    /yad2\.co\.il\/[^\s,]+/gi,
  ];
  
  for (const pattern of yad2Patterns) {
    const match = text.match(pattern);
    if (match) {
      let url = match[0];
      if (!url.startsWith('http')) {
        url = 'https://' + url;
      }
      return url;
    }
  }
  
  return null;
}

